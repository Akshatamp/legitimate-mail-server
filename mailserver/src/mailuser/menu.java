    void refreshmails()
    {
        try
        {
            System.out.println("in refresh mail");
            inbox.clear();
            outbox.clear();
            
            while(dft1.getRowCount()>0)
                dft1.removeRow(0);
            
            while(dft2.getRowCount()>0)
                dft2.removeRow(0);
            
            Socket soc=new Socket(ips.sip,ips.sport);
            ObjectInputStream oin=new ObjectInputStream(soc.getInputStream());
            ObjectOutputStream oos=new ObjectOutputStream(soc.getOutputStream());
            
            oos.writeObject("REFRESHMAILS");
            oos.writeObject(uname);
             inbox=(Vector)oin.readObject();
             outbox=(Vector)oin.readObject();
            oos.close();
            oin.close();
            soc.close();
            
            System.out.println("inbox size: "+inbox.size());
            System.out.println("outbox size: "+outbox.size());
            
            for (int i=0;i<inbox.size();i++)
            {
                ArrayList a=inbox.get(i);
                Vector v=new Vector();
                v.add(a.get(0));
                v.add(a.get(2)+"-"+a.get(3));
                v.add(a.get(5));
                
                dft1.addRow(v);
                        
            }
            
            for (int i=0;i<outbox.size();i++)
            {
                ArrayList a=outbox.get(i);
                Vector v=new Vector();
                v.add(a.get(1));
                v.add(a.get(2)+"-"+a.get(3));
                v.add(a.get(5));
                
                dft2.addRow(v);
                        
            }
            
            
            
            
            
        }
        catch(Exception e)
        {
            System.out.println(e);
            e.printStackTrace();
        }
        
    }
    
    void refreshusers()
    {
        try
        {
            Socket soc=new Socket(ips.sip,ips.sport);
            ObjectInputStream oin=new ObjectInputStream(soc.getInputStream());
            ObjectOutputStream oos=new ObjectOutputStream(soc.getOutputStream());
            
            oos.writeObject("USERSLIST");
            oos.writeObject(uname);
            Vector v=(Vector)oin.readObject();
            
            jComboBox1.removeAllItems();
            for (int i=0;i<v.size();i++)
            {
                jComboBox1.addItem(v.get(i).toString().trim());
            }
            
            if (v.size()==0)
                JOptionPane.showMessageDialog(this,"There are no receivers to send mail!");
            
            oos.close();
            oin.close();
            soc.close();
        }
        catch(Exception e)
        {
            System.out.println(e);
            e.printStackTrace();
        }
    }
        
    
