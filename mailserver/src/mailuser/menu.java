    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // compose send
        
        try
        {
            if (jComboBox1.getSelectedIndex()!=-1)
            {
                String sub=jTextField1.getText().trim();
                String msg=jTextArea1.getText().trim();
                
                if (sub.equals(""))
                {
                    JOptionPane.showMessageDialog(this, "Enter Subject!");
                }
                else
                if (msg.equals(""))
                {
                    JOptionPane.showMessageDialog(this, "Enter Message!");
                }    
                else
                {
                    String sign=new md5hash().getMD5Hash(msg);
                    
                    String ip=JOptionPane.showInputDialog(this,"Enter your IP Address:");
                    
                    Socket soc=new Socket(ips.sip,ips.sport);
                    ObjectInputStream oin=new ObjectInputStream(soc.getInputStream());
                    ObjectOutputStream oos=new ObjectOutputStream(soc.getOutputStream());
                    
                    oos.writeObject("MAIL");
                    oos.writeObject(uname);
                    oos.writeObject(jComboBox1.getSelectedItem().toString().trim());
                    oos.writeObject(sub);
                    oos.writeObject(msg);
                    oos.writeObject(sign);
                    oos.writeObject(InetAddress.getLocalHost().getHostAddress());
                    oos.writeObject(ip);
                    
                    String reply=(String)oin.readObject();
                    
                    if (reply.equals("SUCCESS"))
                    {
                        JOptionPane.showMessageDialog(this,"Mail successfully sent!");
                        jTextField1.setText("");
                        jTextArea1.setText("");
                        refreshmails();
                    }
                    else
                    if (reply.equals("BLOCKED"))
                    {
                        JOptionPane.showMessageDialog(this,"You are blocked!");
                    }
                    else
                    if (reply.equals("RECEIVERBLOCKED"))
                    {
                        JOptionPane.showMessageDialog(this,"Receiver is blocked!");
                    }
                    else
                    if (reply.equals("INVALIDIP"))
                    {
                        JOptionPane.showMessageDialog(this,"Invalid IP entered!\nYou are blocked!");
                    }
                    
                    
                    
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Select Reciver first!");
                        
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

